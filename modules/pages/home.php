        <h1>Our organization</h1>
        <p>TED is a nonprofit devoted to spreading ideas, usually in the form of short, powerful talks (18 minutes or
            less).
            TED began in 1984 as a conference where Technology, Entertainment and Design converged, and today covers
            almost
            all topics — from science to business to global issues — in more than 100 languages. Meanwhile,
            independently
            run TEDx events help share ideas in communities around the world.</p>
        <h3>Our Mission: Spread ideas</h3>
        <p>TED is a global community, welcoming people from every discipline and culture who seek a deeper understanding
            of the world. We believe passionately in the power of ideas to change attitudes, lives and, ultimately, the
            world. On TED.com, we're building a clearinghouse of free knowledge from the world's most inspired thinkers
            — and a community of curious souls to engage with ideas and each other, both online and at TED and TEDx
            events around the world, all year long.</p>

        <p>In fact, everything we do — from our Conferences to our TED Talks to the projects sparked by The Audacious
            Project, from the global TEDx and TED Translators communities to the TED-Ed lesson series — is driven by
            this goal: How can we best spread great ideas?</p>

        <p>TED is owned by a nonprofit, nonpartisan foundation. Our agenda is to make great ideas accessible and spark
            conversation.</p>
